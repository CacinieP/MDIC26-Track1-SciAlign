def main():
    input_path = "data/processed/molalign_dataset.jsonl"
    records = []
    with open(input_path, "r", encoding="utf-8") as f:
        for line in f:
            if line.strip():
                records.append(json.loads(line))

    added = 0
    for r in records:
        cid = r["molecule_id"]["pubchem_cid"]
        has_zh = any(d.get("language") == "zh" for d in r["text_descriptions"])
        if not has_zh and cid in ZH_DESC:
            r["text_descriptions"].append({
                "type": "general_description",
                "content": ZH_DESC[cid],
                "source": {"type": "manual_curation", "reference": f"CID {cid}", "section": None, "mineru_parsed": False},
                "language": "zh"
            })
            added += 1

    with open(input_path, "w", encoding="utf-8") as f:
        for r in records:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    json_path = input_path.replace(".jsonl", ".json")
    with open(json_path, "w", encoding="utf-8") as f:
        json.dump(records, f, ensure_ascii=False, indent=2)

    with_zh = sum(1 for r in records if any(d.get("language") == "zh" for d in r["text_descriptions"]))
    avg_descs = sum(len(r["text_descriptions"]) for r in records) / len(records)
    print(f"Added Chinese descriptions: {added}")
    print(f"With Chinese: {with_zh}/{len(records)}")
    print(f"Avg descriptions: {avg_descs:.2f}")


if __name__ == "__main__":
    main()
